
int bitwise_rshift(int x, int y) {
    return x >> y;
}

int bitwise_lshift(int x, int y) {
    return x << y;
}

int bitwise_xor(int x, int y) {
    return x ^ y;    
}

int bitwise_and(int x, int y) {
    return x & y;
}

int bitwise_or(int x, int y) {
    return x | y;
}

int bitwise_not(int x) {
    return ~x;
}
